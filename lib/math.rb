def addition(num1, num2)
  sum = num1 + num2
  puts sum
  return sum

end

def subtraction(num1, num2)
  difference = num1 - num2
  puts difference
  return difference
  

end

def division(num1, num2)
  quotient = num1 / num2
  puts quotient
  return quotient

end

def multiplication(num1, num2)
  product = num1 * num2
  puts product
  return product

end

def modulo(num1, num2)
  modulo = num1 % num2
  puts modulo
  return modulo

end

